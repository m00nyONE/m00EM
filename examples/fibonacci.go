package examples

import "github.com/m00nyONE/m00EM/memory"

func Fibonacci() {

	/*
		Fibonacci
	*/
	//INIT
	memory.RAM[0x0] = 0x10 //MOV_LIT_REG 0x1, R1
	memory.RAM[0x1] = 0x1
	memory.RAM[0x2] = 0x8

	memory.RAM[0x3] = 0x10 //MOV_LIT_REG 0x1, R2
	memory.RAM[0x4] = 0x1
	memory.RAM[0x5] = 0x9

	//LOOP
	memory.RAM[0x6] = 0x14 // ADD R1, R2
	memory.RAM[0x7] = 0x8
	memory.RAM[0x8] = 0x9

	memory.RAM[0x9] = 0x31 // PSH ACC
	memory.RAM[0xa] = 0x2

	memory.RAM[0xb] = 0x11 // MOV R2, R1
	memory.RAM[0xc] = 0x9
	memory.RAM[0xd] = 0x8

	memory.RAM[0xe] = 0x3F // POP R2
	memory.RAM[0xf] = 0x9

	memory.RAM[0x10] = 0x2F // JMP
	memory.RAM[0x11] = 0x6
}
